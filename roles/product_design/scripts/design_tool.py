# ============= VAVE分析 =============
def vave_analysis(functions):
    """
    VAVE价值分析
    functions: [{"name": "功能名", "cost": 成本, "importance": 重要性(1-10)}]
    """
    results = []
    total_cost = sum(f["cost"] for f in functions)
    
    for f in functions:
        value = f["importance"] / f["cost"] if f["cost"] > 0 else 0
        results.append({
            "name": f["name"],
            "cost": f["cost"],
            "importance": f["importance"],
            "value": value,
            "recommendation": "保留" if value > 0.1 else "优化"
        })
    
    return results, total_cost

# ============= DFMA评分 =============
def dfma_score(checklist_results):
    """DFMA评分"""
    total = len(checklist_results)
    passed = sum(1 for r in checklist_results if r["passed"])
    score = passed / total * 100
    return score, "优秀" if score > 80 else "良好" if score > 60 else "改进"

